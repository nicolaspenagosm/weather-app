export const preloadImage = (src: string) => {
  return new Promise<HTMLImageElement | string>((resolve, reject) => {
    const img = new Image();
    img.onload = function () {
      resolve(img);
    };
    img.onerror = img.onabort = function () {
      reject(src);
    };
    img.src = src;
  });
};
